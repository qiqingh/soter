  YEAR1=00
  MONTH1=01
  DAY1=01
  YEAR2=`get_year $1`
  MONTH2=`get_month $1`
  DAY2=`get_day $1`
  ACCUMULATOR=0
  MULTIPLIER=`expr $YEAR2 - $YEAR1`
  PRODUCT=`expr $MULTIPLIER \* 365`
  ACCUMULATOR=`expr $ACCUMULATOR + $PRODUCT`
  PRODUCT=`days_in_months $MONTH1 $MONTH2`
  ACCUMULATOR=`expr $ACCUMULATOR + $PRODUCT`
  PRODUCT=`expr $DAY2 - $DAY1`
  ACCUMULATOR=`expr $ACCUMULATOR + $PRODUCT`
  echo "$ACCUMULATOR"
